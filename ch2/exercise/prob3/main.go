package main

import "fmt"

func main() {
	var f1 float32 = 123.546789 * 345.678
	var f2 float32 = float32(123.546789) * 345.678

	fmt.Println(f1)
	fmt.Println(f2)
	// 123.546789 = 9자리 정확도. float32는 최대 7자리 정확도(유효숫자)를 가짐
	// f1과 f2의 변환 과정
	// f1 = 123.546789 * 345.678 = float32(42707.406927942) = 42707.406
	// f2 = float32(123.546789) * 345.678 = 123.54679 * 345.678 = float32(42707.40727362) => 42707.41
}
